					<h1>THE ULTIMATE STRENGTH WORKOUT</h1>
					<p>
					Spring is just around the corner, and it is a good time for runners to build their strength and improve race times. With different recommendations in every magazine and website, what is the best way to build strength? Many ultra-runners simply increase the number of miles they run per week in order to improve upon their running. But as a runner myself, I have realized that simply running more miles alone can lead to injury. I have found that Lagree Fitness™ (LF) to be the ultimate strength workout for the ultrarunner and it has lowered my times in all distances from 400m to 50k.
					</p>
					<p>
					Don’t think that LF is some “sissy” strength-training workout. LF offers a low impact, cross training approach and is an excellent way to add strength training to any runner’s routine. This challenging workout is 40 minutes of core crunching, quad burning resistance exercise, all done efficiently on a version of a pilates machine called the ‘Megaformer™’. LF is especially beneficial to runners as it builds strength without creating excess bulk.
					</p>
					<p>
					Performing the same movement over and over when you run leads to the over development of certain muscle groups. A runner endures constant impact while running and their quadriceps become more developed than the hamstrings and the low back becomes stronger than the core. The force of each step travels up from the legs to the lower back and thorax. The core strength that you acquire through LF not only makes those areas better able to deal with the impact, it also improves body alignment and balance. This will help the runner distribute the force of running throughout the body more efficiently, instead of forcing the task onto only a few muscles. Core strength training leads to improved posture, which facilitates easier breathing through increased thoracic expansion. Lagree Fitness focuses on engaging and strengthening core muscles, thus promoting strength and endurance in the abdominal and pelvic muscles.
					</p>
					<p>
					Lagree Fitness™ by design works to promote balance throughout the body. Runners will often develop unilateral weaknesses which can result in injury. Lagree Fitness training improves running form by developing strength in the smaller supporting muscles in all planes, not just muscles used for forward propulsion. Lagree Fitness accomplishes this by lengthening, strengthening, and leaning out the body. That equates to more flexibility and strength without bulk. By developing the stabilizing muscles and building a stronger running support system throughout the body, you will protect the knees, hips, and low back, reducing your chance of injury and improve power during intense running effort. Weak abdominal and gluteus muscles lead to breaks in form. Retraining and strengthening the muscles of the core to work synergistically, will improve running efficiency and speed recovery.
					</p>
					<p>
					The core principles of LF training are: 1) Progressive Overloading, 2) Slow and Controlled Movement, 3) Constant Tension, and 4) Multiple Muscle Group Recruitment to Muscle Failure. Simply put – a muscle will develop a stimulus to grow and strengthen when it is forced to operate beyond its comfort zone. LF exercises accomplish this by having the muscles contract slowly, without momentum through the entire range of motion and remain with some tension throughout the set. This results in recruiting all the muscle fibers (fast and slow twitch) progressively. Rapidly contracting muscles against a load makes them prone to tearing. The slow, controlled nature of this training is what prevents injury. The unique design of the sessions work each muscle group to failure and this is the main stimulus to the muscle to signal it to improve.
					</p>
					<p>
					Pilates originally was created to be mat exercises, but eventually evolved into using a machine with springs to increase the resistance. The reformer machine is the basic tool used by traditional Pilates instructors. One of the main advantages of LF over traditional Pilates is that it uses a modified form of this machine, the ‘Megaformer’ developed by its creator, Sebastien Lagree (Figure 1).
					</p>
					<p class="text-center mrgT30 mrgB30"><img src="images/science2-img2.jpg" alt="image" class="img-responsive" /></p>
					<p>
					Like the traditional machine, it has a moveable carriage, straps and pulleys, and adjusted with springs of different tension. However, the ‘Megaformer™’, now in its third generation (named the M3), has a longer stride attached to its carriage, and bars off both the front and back. The long carriage travel modification, makes it more versatile for standing exercises, and allows both a push and pull effect for muscular training. It is also the key aspect of training balance, as you are forced to balance the weight of the entire body while standing on, and moving the carriage. The use of these special springs is the key to the safety and efficiency of these workouts. Sebastien continues to improve and advance the machine. He is currently putting the finishing touches on the M3, which has added some very practical refinements, such as a smoother operation and more ergonomically placed handles, pads, markings and springs adjusters. Compared to a traditional Pilates reformer, the M3 is like a ‘Ferrari’. His attention to its constant evolution will ensure the best platform possible to maximize fitness potential. Interestingly, LF does not focus on flexibility training. If you are looking specifically to increase flexibility, I would think more about hot yoga, as the LF instructors work muscular strength as the focus. Given that the training is moving muscles both a positive and negative direction, elongating the muscle, you will see increased movement around your joints. The instructors do not focus on actual stretching, so you should stretch on your own, before and after class. The classes are 40 minutes long, and have a very short warm up, with no breaks. The instructors most times will set up the machine for the next exercise while you are finishing the previous one, keeping your body in constant motion. A typical LF workout will target the abs, obliques, legs, glutes, hips, arms and back (Figure 2 a-g).
					</p>
					<p class="text-center mrgT30 mrgB30"><img src="images/science2-img3.jpg" alt="image" class="img-responsive" /></p>
					<p>
					The workout varies from day to day so you never get over trained in one area, making sure you get training to essentially every muscle group. This is not a cardio type of workout. While it does raise your heart rate, you will not be breathless, or significantly sweaty. Your muscles will be completely spent, as you work your muscles to absolute exhaustion. Runners will find this type of training extremely challenging, despite their ultra endurance, as the muscles worked during LF will most certainly be weak when you first start. By varying your body position on the machine you can adjust difficulty on the fly. You might think this will eventually get easy and you can master it. While you certainly will get stronger, it NEVER gets easy. As you improve, the instructors add more springs making it forever challenging. I have experienced that LF is delivered by a dedicated team of highly accomplished instructors, enabling athletes of all levels and ages to achieve their fitness goals. Personal attention, small classes, and an intimate training environment help LF maintain their exceptional reputation of success in all areas of athletic performance. You can find a studio in your area by checking the website: www.lagreefitness.com.
					</p>
					<p>
					I recommend beginners use the easy options when it comes to spring choice or body position. Many times the exercise will seem easy, but the instructors will do multiple similar exercises designed to work the muscle to exhaustion, and you will pay in suffering (and soreness the next day) for trying to be a hot shot. The amazing thing is how hard you can push your body without getting injured. This aspect of the training is especially important for master runners, who are trying to maximize race times with strength training. Injuries in older runners can result in prolonged down times.
					</p>
					<p>
					UltraRunning is our lifelong passion, but it can lead to muscular imbalances. I have been practicing Lagree Fitness at the ‘CORE40’ studio, in North Park San Diego, for over a year and use this cross training to keep a strong, balanced body and have experienced overall better race results. Without doubt, I have noticed more power in my running and significantly less injuries. As a doctor, I firmly believe that Lagree Fitness can help runners alleviate muscle imbalances, help with balance, improve posture, assist with breathing and build a strong core. Every ultrarunner should think about incorporating Lagree Fitness into their workout.
