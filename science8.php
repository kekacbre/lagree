					<h1>GUIDELINES FOR THE PRACTICE OF LAGREE FITNESS DURING PREGNANCY</h1>
					<h3>ABSTRACT</h3>
					<p>
					Lagree Fitness TM, uses a spring based resistance machine, similar to a Pilates reformer, called a Megaformer TM. The workout is becoming more popular with women of childbearing age, many of whom will become pregnant. Although similar to a reformer, there are key differences in both the equipment and the training philosophy that makes this activity challenging for the pregnant participant and trainer. We present guidelines for the practice of Lagree Fitness, during and after pregnancy, based on the current available literature and our experience with its use in parturients.
					</p>
					<h3>INTRODUCTION</h3>
					<p>
					Lagree fitness (LF) is a spring-based resistance training system. It is performed on a pilates reformer like machine called a Megaformer (1). While there are some similarities to the reformer, there are significant differences (Figure 1). Due to the larger surface and sturdy construction, a subject can stand on both the platforms and carriage of the machine.
					</p>
					<p>
					In addition, the set up of the springs and carriage allows for exercise on both the front and back, which have opposing functions. When exercising on the front of the machine, adding springs will assist making it easier to do the exercise. This is helpful when one is treating patients with injuries, deconditioning or neurologic dysfunction. When performing the exercises on the back of the machine, adding springs will increase the amount of effort needed. This is used for experienced subjects and athletes.
					</p>
					<p>
					The philosophy of a LF training session is to perform the exercises slowly, so as to engage the target muscle and keep it under load as long as possible until the subject experiences discomfort or muscular failure. A typical session will work the entire body, one part at time. It normally includes legs, core, upper body including arms, shoulders and chest, and obliques. In a typical training session, the instructor would focus work on side of the body at a time, until the target muscles are exhausted, and then switch to the other side. For parturients, there are modifications and specific exercises, which need adjustment as pregnancy progresses. Before presenting LF specific recommendations, exercise physiology and expert recommendations of exercise during pregnancy will be presented.
					</p>
					<p class="text-center mrgT30 mrgB30"><img src="images/science8-img2.jpg" alt="image" class="img-responsive" /></p>
					<h3>PHYSIOLOGIC CHANGES DURING PREGNANCY</h3>
					<p>
					Exercise during pregnancy is generally safe and desirable. The U.S. Department of Health and Human Services issued physical activity guidelines for Americans.
					</p>
					<p>
					In which healthy pregnant and postpartum women, should have at least 150 minutes per week of moderate-intensity aerobic activity (2). The World Health.
					</p>
					<p>
					Organization and the American College of Sports Medicine have issued evidence-based recommendations indicating that the beneficial effects of exercise in most adults are indisputable and that the benefits far outweigh the risks (3,4).  According to the American Congress of Obstetricians and Gynecologists (ACOG), from an individual health perspective, moderate regular exercise during pregnancy may decrease the incidence of diabetes and limit excessive weight gain (5,6,7). It will also decrease post partum deconditioning. There is some evidence that pre-conception fitness training will decrease complications of delivery and improve return to pre-pregnancy fitness level sooner (8).
					</p>
					<p>
					Several physiologic changes occur which will affect the parturients ability to participate in strenuous exercise. These include the following: baseline increase in heart rate, cardiac output, stroke volume, blood volume and a decrease in systemic vascular resistance (SVR) (9,10). Due to these changes dizziness may be present during exercise in pregnancy. There are also changes in the respiratory system including: an increase in respiratory rate, minute ventilation, and an decrease in pulmonary reserve (11). These changes may lead to increased breathlessness during vigorous exercise.
					</p>
					<p>
					After 20 weeks, there is a potential for decreased venous return and hypotension while lying supine, due to compression by the gravid uterus (Figure 2) (12,13,14). Moving the patients position to left side down will displace the uterus and relieve the obstruction.<br>
					Temperature regulation is impaired during pregnancy. Parturients may have difficulty dissipating heat and thus and should not exercise in heated rooms.  Hyperthermia during organogenesis is associated with neural tube defects (spina bifida) and should be avoided (15,16). Later in pregnancy, the fetus cannot dissipate the unwanted heat, and thus may become uncomfortable (17,18).
					</p>
					<p>
					Weight gain, and uterine enlargement can shift in the point of gravity, causing instability increasing the possibility of a woman to fall during exercise. There is an increased laxity of joints which can cause hyperextension and injury(19). This may be due to an increased level of the hormone relaxin. While there definitive evidence of both the increased relaxin levels and the hypermobility, the cause and effect is not certain (20,21). The increased mass from the growing fetus, may put excessive force on joints leading to injury. Care should be taken during the later stages of pregnancy not to place the parturient in a situation where falls are possible. From a nutritional standpoint, a woman has an increased need for total calories due to growing fetus. For most women pregnancy is not a time for calorie-restricted diets. Thus fasting, especially before exercise should be avoided.
					</p>
					<p class="text-center mrgT30 mrgB30"><img src="images/science8-img3.jpg" alt="image" class="img-responsive" /></p>
					<h3>SAFETY</h3>
					<p>
					Since there are many reasons for parturients to exercise, its safety should be studied. In the past there were concerns of miscarriage, poor fetal growth or premature birth. In healthy women, especially for those who are active there is good scientific evidence to support the safety of exercise in moderation. Studies looking at the fetal heart rate response, show a modest increase in fetal heart rate with vigorous exercise, but is otherwise well tolerated (22). There does not seem to be an increase in preterm delivery or serious complications and may decrease pre-eclampsia and the need for operative deliver (23, 24, 25 26, 27). There is a trend to have smaller babies with heavy exercise, although this effect was not uniform in all studies (28,29).  Pregnancy is not the ideal time for serious competition. Competitive athletes may need to decrease their level of activity somewhat depending on the growth of their fetus, but aside from these issues the woman and her doctor should be the judge of her level of intensity.
					</p>
					<p class="text-center mrgT30 mrgB30"><img src="images/science8-img4.jpg" alt="image" class="img-responsive" /></p>
					<p class="text-center mrgT30 mrgB30"><img src="images/science8-img5.jpg" alt="image" class="img-responsive" /></p>
					<p>
					Lagree fitness, while not specifically mentioned in the ACOG guidelines would be considered safe (Figure 3). Women with medical or obstetric comorbidities  (Figure 4,5) may need to refrain from intense exercise and have their situations analyzed individually. All women should undergo a thorough evaluation to make sure there is no medical contraindication to exercise. Every woman, regardless of pre-pregnancy fitness level should obtain clearance from her physician before embarking on exercise during pregnancy.
					</p>
					<h3>DAMAGE TO THE RECTUS ABDOMINIS</h3>
					<p>
					The rectus abdominis is a paired superficial muscle, superficially located on the abdominal surface that flexes the trunk (six pack). It is a paired muscle connected by a thin layer of connecting tissue called the linea alba. Rupture of the connective tissue, is called diastasis recti (DR), or a separation of the belly of the rectus. This is fairly common during normal pregnancy as the uterus expands with the growing fetus (30). There is some evidence that women with strong abdominal muscles pre-pregnancy will have a decreased risk of diastasis during the pregnancy. There are many recommendations (although very little scientific evidence) that abdominal training may cause DR (31,32). It is also proposed that specific training of the deep abdominal muscular layers, the transversus abdominis, and internal and external obliques may prevent separation of the rectus abdominis.
					</p>
					<h3>EXERCISE IN THE POST PARTUM PERIOD</h3>
					<p>
					Rapid return to physical activity is important to promote a healthy lifestyle and for maternal psychological well-being. Even vigorous exercise post-partum should not affect milk production or the mothers ability to breast feed (33). Strengthening the transverse abdominis and pelvic floor muscles are important to recover normal core strength. Women who are post partum should be cleared by their physician before returning to vigorous physical activity. Typically this is 8weeks after birth or later. A woman should return to resistance exercise slowly until her muscles have recovered baseline tone and strength. Post-partum clients should not be pushed beyond physical discomfort, due to the physical impact pregnancy has on abdominal-pelvic musculature. Failure to heed this may lead to injury.
					</p>
					<h3>GENERAL GUIDELINES FOR EXERCISE DURING PREGNANCY</h3>
					<p>
					1. Parturients should avoid heat throughout pregnancy. Heat is a teratogen in first trimester and can cause fetal distress in later pregnancy. The internal body heat generated by Lagree fitness should be well tolerated in a cooled environment.<br>
					2. Parturients should be well hydrated and allowed free access to water during the workouts.<br>
					3. Training while prone or supine should be done with caution after the first trimester In the later stages of pregnancy, it is imperative to avoid the supine hypotensive syndrome. A rapid fall in blood pressure and perfusion to the fetus. Immediate change to lateral position will immediately resolve the physiologic changes<br>
					4. Avoidance of falls and trauma, is obvious, but should take priority over maximizing the fitness benefit of the activity.<br>
					5. Pregnancy is not the time for competitive training. Working on deep abdominal and pelvic strength is helpful for delivery and post partum.<br>
					6. The pregnant client should listen to her body. She should not be pushed beyond her comfort limits. Good communication between the instructor and client is essential for safe training..
					</p>
					<h3>LAGREE FITNESS SPECIFIC GUIDELINES</h3>
					<p>
					For a summary of exercise guidelines see figures 6-9.
1. Even early in pregnancy, the cardiovascular changes including increased heart rate and stroke volume, coupled with decreased peripheral resistance, may cause dizziness upon standing. Thus it is critically important to have a way to hold on if the client becomes unbalanced. Having a pole in hand at all times when standing is critically important and should be enforced for all parturients, regardless of gestation. Also risky moves such as super lunge, 5th lunge where there is no hand support while standing should be avoided.<br>
2. Exercises at the back of the machine are more stressful. Once the parturient is in the 2nd trimester, clients should be encouraged to use do an equivalent exercise on the front of the machine<br>
3. Once the client is in the mid second trimester, they should not be allowed to lie either prone or supine. This is to prevent the supine hypotension syndrome. Exercises lying Right side down should be minimized.<br>
4. Post-partum exercises should be delayed until the woman is ready and cleared by her physician (6-12 weeks depending on the mode of delivery). Exercise before this time should not be a Lagree Fitness class. The Megaformer can be used for gentle rehabilitation type activity, but this should be one on one training, and only by someone qualified to use the equipment in this capacity. The post partum client should be encouraged to slowly increase her activity as tolerated. She should not be pushed past her physical limits in the immediate post partum period. Many women will be anxious to get going and get back to their pre-pregnancy fitness, and body shape. Due to the mechanical disruption in their core musculature, and attachments to the pelvis and ribs, resistance based training should increase in intensity and frequency slowly.<br><br>
a) Exercises should start on the front of the machine, and use springs to support the core initially. Avoid giant wheelbarrow, and those abdominal exercises where there is no spring support. Advise the post partum client to be careful with rectus abdominis strengthening until the deep abdominal layers have returned to baseline fitness. Consider initially using up to 3 yellow springs for front of the machine core work (wheelbarrow, bear, plank to pike)<br>
b) Carefully add inner thigh strengthening as the adductors may have been stretched, and deconditioned. The attachment to the pelvis may be weakened. Any inner thighs work should be done on the front of the machine, and with a pole for support.<br>
c) The post partum client should be encouraged to continue to use the pole for standing exercises until fitness is back to baseline<br>
d) Consider doing hip thrusts instead of bungee for gluteus maximus strengthening due to the weakened support of adductors post partum.
					</p>
