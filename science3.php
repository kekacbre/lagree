					<p>
					Put most simply, poor posture is any way the body is held creating awkward posture. Poor posture can result frorn habitual positioning that causes unnecessary strain on the body. When the body is forced to hold itself in a way that causes pain and discomfort, that can be thought of as poor posture. This is in contrast to 'Lazy Posture', where by avoiding postural challenge overtime, leads to deconditioning. The decondition body then slumps over looking less than ideal. Weakness over time can lead to pain, causing a cycle of worsening posture, pain and further weakness. From the athletic perspective of Lagree Fitness trainers, over-training in one plane can lead to imbalances, which will then affect the body's posture.
