					<h1>POSTURE AND ATHLETIC TRAINING : PART 2</h1>
					<h3>CAN WE AS TRAINERS CHANGE OUR CLIENTS POSTURE FOR THE BETTER OR SHOULD WE EVEN TRY?</h3>
					<p>
					One thing is for sure, poor training can exacerbate bad posture. The most important way we can influence a clients posture with Lagree Fitness is by strengthening the core muscles in both directions, and in all 3 planes, creating a connection between brain and muscles. One of the problems with typical athletic training is to focus solely on 'Abs'. Clients are often told by health professionals to 'strengthen their core', which to many people means doing situps and other similar gym machine exercises argeting the rectus, and hip-flexors.
					</p>
					<p>
					Even using the Megaformer, it is possible to target the core in several planes, but not in both flexion and extension (in the sagittal plane). It is easy to target the abs with giant crunch, super crunch and Megaformer crunches in flexion. Harder to train correctly, is working the trunk sagittal plane in the extension direction. Overtraining in flexion, will lead to tight hip flexors, and tends to pull on the lumbar vertebrae straining the nerve roots in some individuals with lumbar spine disease. Some flexion xercises themselves can cause pain due to direct compression of nerve roots and the clients with spinal disease should not be encouraged to work through the pain.
					</p>
					<p class="text-center mrgT30 mrgB30"><img src="images/science4-img2.jpg" alt="image" class="img-responsive" /></p>
					<p>
					Direct activation of the trunk extensor muscles can be done with the exercise 'swimmer'. It is important to emphasize contraction of extensor muscles and hold the exercise in the extended position for a few seconds, in the way the Bikram Yoga 'Full Locust' pose is held. Another exercise is reverse situps on the back platform with the heels underneath the x-strap. More important than isolated back extension is to have the clients activating their back muscles during any of the arm and shoulder exercises that have the client kneeling position. Chest opener and serve the platter are good examples of exercises that strengthen the back muscles in the extension plane, even though there is no motion in that direction. When doing chest opener, to ensure the client is activating the back extensor muscles, including glutes, hamstrings, erector-spinal, the instructor should use verbal and tactile cues. It is also important to activate the rhomboids and trapezius muscles bringing the shoulder blades together. Once the glutes, hamstrings and back muscles are activated, then have the client move the arms pivoting at the shoulder joint.
					</p>
					<p>
					This will bring attention to the core in the extensor direction and bring awareness which will last long after the training session is over. While strengthening the muscles is therapeutic on its own, creating a strong neural connection between the central nervous system and the target motor neurons is equally important. It is likely that developing awareness of the feel and function of these extensor back muscles will have a lasting benefit during periods of gravitational and environmental stress.
					</p>
					<p>
					For example, when sitting at a computer, a client who has regular exposure to extensor muscle strengthening and awareness will be able to sit up straighter and with less effort and greater comfort then the untrained. It is important to point this out to the clients to ensure they understand the connection to lasting benefit.
					</p>
					<p>
					New to the Lagree Fitness lineup, the new SupraformerTM, has options that allow the instructors to change the angle of the training surface in both the vertical and horizontal plane. Having the ability to change rotate the surface from side to side, opens tremendous opportunities to stress the body in the extensor direction. Consider performing torso twists, on a flat surface. This exercise stresses the obliques, hamstings and glutes. When the entire machine is angled even a few degrees the body must work significantly harder to maintain an upright posture. This gives the body a larger stimulus for neurologic and muscular improvement. Once widely available, there will be many new ways to unlock the potential of this new device.
					</p>
					<p>
					Monitoring that all of the stabilizing muscles are activated properly will ensure that the body is benefiting from the posture improving effect of these exercises. If the instructor notices a client is not using proper form despite adequate verbal cues, than it is helpful to use tactile stimulation to ensure they are activating the proper muscles. Hands on adjustments are the most effective way to promote the brain muscle connection. Even doing exercises like super crunch, typically an abdominal exercise that primarily targets the rectus abdominis, has the potential to potential to work back muscles. When setting up the form for this exercise, make sure to instruct the client to pull the arms back, lift the chin up and bring the shoulders back and together before flexing at the hip. Even though the primary mover is the rectus abdominus, the back extensors, rhomboids and glutes are being activated as stabilizers, bringing attention to the muscles of the back.
					</p>
					<p class="text-center mrgT30 mrgB30"><img src="images/science4-img3.jpg" alt="image" class="img-responsive" /></p>
					<p>
					This brings forth the broader issue that nearly every exercise on a Lagree machine whether it be the megaformer M3S or the SupraformerTM, should be taught encouraging dynamic functional movement to build strength alignment and endurance. Not only does swimmer and chest opener engage the three planes in both directions, but Wheelbarrow, Lunges and quite frankly every move done on the machine with proper form, reinforces good posture if cued and performed correctly. Many clients struggle to roll their pelvic floor forward under a load, to keep a more neutral lower back. This break in form, anatomically reduces the natural lumbar lordosis and puts strain on the lower spine and neural tissue. Having a proper lumbar curve, allows one to draw the shoulder down and back and engage the extensors of the upper back, keeping the 3 natural curves intact during forcible loads during the different exercises.
					</p>
					<p>
					In conclusion, performing exercise to strengthen the muscles that support the spine in all three planes, in both directions, are most likely to have a beneficial effect on allowing the body to assume a posture that is most efficient for activities of athletics and those of daily life. Instructors should be comfortable with hands on adjustments to allow a brain-muscle connection, and to ensure the clients are activating the correct muscles during all the exercises of a Lagree Fitness training session. Its important to remind the clients that these neurologic connections and increased muscular strength will remain so they can more comfortably assume a less awkward posture throughout the stresses of their day. Therapies, which align the spine but do not improve strength, movement and neurologic function, are not likely to be effective.
					</p>
