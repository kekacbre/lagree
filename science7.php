					<h1>THE LAGREE MEGAFORMER, (WITH THE MOST CURRENT VER- SION, THE M3S AND SUPRAFORMER) IS A SPRING BASED RESISTANCE TRAINER, WHICH CAN BE USED AS A THERAPY DEVICE</h1>
					<h3>ABSTRACT</h3>
					<p>
					The Lagree Megaformer, (with the most current version, the M3S and Supraformer) is a spring based resistance trainer, which can be used as a therapy device, to train patients with neuromuscular deconditioning. Due to the mechanical workings of the machine, the springs, which normally confer the resistance, can also be used to support the patient during exercise. Once the patient has regained function, support can be gradually removed and resistance added until the patient is fully recovered and beyond. We recommend physical therapists, chiropractors, sports medicine physicians other practitioners treating debilitated patients with neuromuscular disorders consider using this device as part of a therapeutic treatment plan.
					</p>
					<h3>INTRODUCTION</h3>
					<p>
					Since its introduction into the fitness world by creator, Sebastien Lagree, the Lagree Fitness (LF) method with its spring based trainer the Megaformer, has been a state of the art tool for body sculpting and improving dynamic fitness. Its utility is apparent in subjects new to exercise as well experienced athletes. Through several redesigns, the machine has evolved to devices (Megaformer M3S, Supraformer) which can be used to work any part of the body, especially the core musculature, in all planes (Figures 1&2).
					</p>
					<p class="text-center mrgT30 mrgB30"><img src="images/science7-img2.jpg" alt="image" class="img-responsive" /></p>
					<p>
					While there are many differences compared to the traditional Pilates Reformer, a unique feature of the Megaformer is the ability for the subjects to stand on the surface, training balance as well as strength. While doing LF training, the subject is taught to move the target muscle through the full range of motion very slowly, to sequentially recruit muscle fibers until muscular failure occurs.
					</p>
					<p>
					Once the muscle has been worked to exhaustion, the subject’s myocytes will be stimulated to increase their capacity for work through hypertrophy. The net effect in athletic subjects is increased muscle strength and definition. It also promotes the neuro-muscular connection needed to train stability musculature often neglected in standard weight training.
					</p>
					<p class="text-center mrgT30 mrgB30"><img src="images/science7-img3.jpg" alt="image" class="img-responsive" /></p>
					<p>
					When using the Megaformer in patients with severe debilitation, or neurologic damage, modifications are needed to accommodate the disability. When used effectively LF training can stimulate the missing neuromuscular connection, improving muscular strength and endurance. The aims of this case report is to illustrate the outcome of a patient treated with LF training and describe some of the modifications to the typical LF training session needed when treating deconditioned and disabled patients..
					</p>
					<h3>CASE REPORT</h3>
					<p>
					The patient is a 38 year-old male with severe atrophy and weakness, primarily in his lower extremities, below the knee (Figure 3). The patient had normal growth and development, and was participating in cross-country running until age 17. At that time he began to notice
					</p>
					<p class="text-center mrgT30 mrgB30"><img src="images/science7-img4.jpg" alt="image" class="img-responsive" /></p>
					<p>
					increasing weakness, especially in his lower legs. Over the next few years he became progressively weaker in his legs until he was no longer able to ambulate, using a motorized wheel chair and walker to get around.
					While he did experience some weakness in his hands, his cognition and motor function above the knees were preserved. His diagnostic evaluation was extensive including: MRI, EMG, Nerve conduction studies, and blood tests. These tests were all non-diagnostic. Genetic testing including exon analysis was done, but did not lead to a diagnosis. He has mildly elevated CPK at baseline (125mg/dl), but his other labs are normal. EMG and nerve conduction are consistent with atrophy and slow neural transmission.
					On physical exam he had essentially no function and extreme wasting of his gastrocnemius and tibialis anterior. He had 2/5 strength in hamstrings, Quadriceps and Gluteus Maximus muscles, bilaterally. His hip flexors and abdominal musculature function, while deconditioned was intact. He had 4/5 strength in his upper extremities, except for the intrinsic hand muscles, which had mild weakness and contractures.
					</p>
					<p>
					On presentation he was able to perform abdominal exercises, but was unable to lunge or squat to any degree. He had extreme difficulty using a walker. After 20 years, there was still no medical diagnosis and his strength showed no signs of spontaneous recovery. He had tried traditional physical therapy but he felt he was not improving. The patient made the decision to try exercise therapy of his own choice. His rehabilitation program included daily Bikram hot yoga and two 30 minute, LF sessions per week. Initially he was unable to stand or do any standing exercises without assistance. The initial LF sessions focused on strengthening his core muscles in all 3 planes, in both flexion and extension, while gently working the upper legs, and arms. The ultimate goal was self-ambulation.
					</p>
					<p>
					Due to his severely weak lower legs, the initial target of the therapy was his upper legs and hips. In contrast to standard LF workouts where a single side of a target muscle group is worked to exhaustion, in this patient, we limited the workout to one exercise per side at a time. This was done to allow more recovery time so he could get more total workload. A sample exercise set is displayed below (Figure 4).
					</p>
					<p>
					Over the course of 6 months he was gradually able to increase the load and duration of the sets. His form during the exercise sessions significantly improved, as he was very unstable at the beginning of the program. Over the first six months, we were able to add squats and lunges, first assisted then unassisted. To add lunges, the patient was started with the ‘side lunge’ exercise and the springs were adjusted to give maximal assistance (3-4 yellow springs). As he progressed he was able to tolerate removal of springs due to increased strength in quads, glutes and hamstrings. The most remarkable improvement was his ability to conUsing the Lagree Megaformer | With Neuromuscular Dyfunction : A Case Study 2 trol the movement and position of his legs. This was accomplished using visual and tactile cues from the therapist while repeating simple movements over many repetitions.
					</p>
					<p>
					Over time he was able to regain control his movements, which showed shaking and poor coordination at the start of the therapy. During the later portion of his rehabilitation, muscular endurance improved to the point he was able to swim laps, bike 20 miles, and walk over 1 mile without assistance. To provide motivation, we made a goal to compete in an endurance race at his one year anniversary. Recently he was able to achieve that goal and finish a mini sprint triathlon after 12 months, of continuous training. Figures 5 ,6.
					</p>
					<h3>DISCUSSION</h3>
					<p>
					The Lagree fitness method using the Megaformer, because of its innovative design is optimally suited to rehabilitation type exercise. While known primarily for its use in the fitness industry, the Megaformer has several mechanical features, which make it suitable for use in rehabilitation. It is a very solid and stable platform. Even morbidly obese patients will be able to use the machine safely. The patient can be sitting, lying down supine, prone, or standing, which is helpful for disabled patients.
					</p>
					<p>
					With over 300 defined exercises, and countless user described modifications there is a very large selection of ways to train each muscle group. Their springs provide many different resistances, so adjusting the resistance to even severely weak patients feasible, as in this case. In addition, counter intuitively, exercises done on the front of the machine, get easier as resistance is added.
					</p>
					<p class="text-center mrgT30 mrgB30"><img src="images/science7-img5.jpg" alt="image" class="img-responsive" /></p>
					<p>
					This is ideal for the weak patient, since many will only be able to output a small amount of force. While doing exercises on the front of the machine (like ‘side lunge’), the therapist can add springs which support the patient while allowing them to experience work at the target muscles. This is accomplished by increasing the number of yellow springs (the lightest tension) on the machine. This is in contrast with most gym equipment, which the patient is required to do all of the work, creating frustration during therapy.
					<br>
					There are several other differences between training fitness clients and rehabilitation patients (Figure 5 ). Setting up the sequence of exercises is especially important with the more disabled patients. Its important not to push the patient past his physical limits to ensure they can get through the session without severe discomfort or injury.
					</p>
					<p>
					Unlike the typical fitness client, the rehabilitation patient will need more recovery between sets. Initially it may be helpful to perform the exercise on the left then right, instead of working one side to exhaustion, as is normally done for fitness training. Another difference is setting up the patient to promote stability. This enables the patient to focus on the exercise without the likelihood of falling over or injury. This is in direct contrast to fitness clients.
					</p>
					<p>
					While training to maximize fitness gains, purposely placing the subject in positions of instability forces the client to use stabilizing muscles, unlike when using standard fitness equipment like a universal gym machine. This is a definite benefit of LF training for athletes. The rehabilitation patient will need more supervision and assistance than typical fitness clients. At least initially, these patients should not be treated in a class environment. Individual supervision will improve safety and decrease frustration. Advanced planning and evaluation of the patient’s injury, or current physical status, will ensure the patient receives customized exercise, based on their specific disability. As the patient improves, they should require decreasing supervision, enabling possible transition to group classes.
					</p>
					<p>
					Unlike a physical therapy session, the LF training ideally should be accompanied by music that the patient is excited to hear. This will increase the intensity and enjoyment of the effort.
					</p>
					<p>
					It is important to realize, that some disabled patients may be able to do very little work during the initial sessions. Independent of the improvement in muscular strength, the patient will make neuro-muscular connections that will mature over time, through re-wiring.
					Many of these patients are extremely deconditioned, requiring patience on the part of the therapist/ instructor. It is important to discuss the expectations of the therapy. Unlike what is popularized on television, complete reversal of the disease through miracle cures will not be likely. However, with consistent training, slow steady gains can occur. Be careful to avoid overtraining and injury.<br>
					Initially 1-2 session per week is sufficient for most patients to allow time time for adequate recovery. This can be increased to 3-4 sessions as the patients’ muscular strength and endurance improve. Also it is critical to review the patients’ nutritional intake to maximize the muscular gains, which occur between training sessions.
					</p>
					<p>
					Rehabilitation through physical therapy can be extremely challenging for patients with advanced neurologic disease. It takes dedication to any program if the patient is to receive benefit. Diagnoses of depression may also be present, slowing the rehabilitation process. The patients may be experiencing other coexisting medical disease, such as cardiac failure, COPD, sleep apnea, diabetes mellitus in addition to their neurologic dysfunction.
					</p>
					<p>
					Medical evaluation and clearance will be necessary before starting any rehabilitation program. While physical and occupational therapy is the standard of care for rehabilitation, other modalities can play a positive role in successful recovery. Yoga, endurance and strength training all will benefit patients who are motivated to recover and are able to tolerate the effort needed to perform these activities.
					</p>
					<p class="text-center mrgT30 mrgB30"><img src="images/science7-img6.jpg" alt="image" class="img-responsive" /></p>
					<h3>CONCLUSION</h3>
					<p>
					Rehabilitation of deconditioned muscles is a difficult and complex process. In many cases neurological and psychological dysfunction is ongoing. Lagree Fitness training with the Megaformer can be a valuable tool in this patient population. Disabled patients can improve muscular strength, endurance, coordination, and balance using a modified version of LF training.
					</p>
					<p>
					For appropriate patients in a well monitored and controlled environment, LF training can be used to reverse the debilitating effects of many neurologic disorders. Medical clearance should always be sought before starting any rehabilitation program.
					</p>
